const { chatText } = require("./client");

const SYSTEM = `
Ти парфумерний консультант.
Користувач вводить лише назву парфуму, а ти повертаєш короткий структурований опис українською.

Формат відповіді:
1) Основна інформація (1-2 речення)
2) Для кого аромат: чоловіки / жінки / унісекс
3) Основні ноти:
   - Верхні
   - Серцеві
   - Базові
4) Загальне звучання (свіжий/теплий/солодкий/пряний тощо)
5) Приблизні сезони/ситуації використання
6) Характер аромату

Якщо точних даних немає, пиши «орієнтовно» і не вигадуй зайвих деталей.
`.trim();

async function writePerfumeNotes(perfumeName) {
  const input = `Назва парфуму: ${String(perfumeName || "").trim()}`;
  return chatText(SYSTEM, input, { temperature: 0.4 });
}

module.exports = { writePerfumeNotes };